# Exercício 3.7
# Faça um programa que peça dois números inteiros.
# Imprima a soma desses números na tela.

numero_A = int(input('Digite um número inteiro qualquer: '))
numero_B = int(input('Digite outro: '))

soma_AB = numero_A + numero_B

print(f'\nA soma é: {numero_A} + {numero_B} = {soma_AB}.')
input('\nPressione Enter para sair... ')
